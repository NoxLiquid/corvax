using Content.Shared.Humanoid.Prototypes;
using Content.Shared._Modifications.Disease.Prototypes;
using Content.Shared._Modifications.Disease;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;
using Robust.Client.UserInterface.Controls;
using Content.Client._Modifications.Disease.Systems;

namespace Content.Client._Modifications.Disease.UI;

[GenerateTypedNameReferences]
public sealed partial class DiseaseEvolutionConsoleWindow : DefaultWindow
{
    [Dependency] private IPrototypeManager _prototype = default!;
    [Dependency] private IEntityManager _entityManager = default!;
    private DiseaseEvolutionConsoleBoundUserInterfaceState? _lastUpdate;
    private readonly List<DiseaseSymptomPrototype> _availableSymptoms = new();
    private readonly List<SpeciesPrototype> _availableBodies = new();
    private ProtoId<DiseaseSymptomPrototype>? _selectedActiveSymptom;
    private ProtoId<SpeciesPrototype>? _selectedActiveBody;


    public DiseaseEvolutionConsoleWindow()
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);

        TabContainer.SetTabTitle(EvolutionTab, Loc.GetString("disease-evolution-tab-evolution"));
        TabContainer.SetTabTitle(WhitelistTab, Loc.GetString("disease-evolution-tab-whitelist"));

        AvailableSymptomsList.OnItemSelected += OnAvailableSymptomSelected;
        AvailableBodiesList.OnItemSelected += OnAvailableBodySelected;

        ActiveSymptomsList.OnItemSelected += OnActiveSymptomSelected;
        ActiveBodiesList.OnItemSelected += OnActiveBodySelected;

        DeleteSymptomButton.Disabled = true;
        DeleteBodyButton.Disabled = true;
    }

    public void Populate(DiseaseEvolutionConsoleBoundUserInterfaceState state)
    {
        _lastUpdate = state;
        MutationPointsLabel.Text = Loc.GetString("disease-evolution-mutation-points", ("points", state.MutationPoints));
        WhitelistMutationPointsLabel.Text = Loc.GetString("disease-evolution-mutation-points", ("points", state.MutationPoints));

        // Статистика вируса
        if (!state.IsSentientDisease)
        {
            DiseaseHealthLabel.Visible = false;
            InfectivityLabel.Visible = false;
            InfectedCountLabel.Visible = false;
            PointsPerSecondLabel.Visible = false;
        }
        else
        {
            DiseaseHealthLabel.Text = Loc.GetString("disease-evolution-health", ("max", (int)state.MaxThreshold));
            InfectivityLabel.Text = Loc.GetString("disease-evolution-infectivity", ("percent", (int)(state.Infectivity * 100)));
            InfectedCountLabel.Text = Loc.GetString("disease-evolution-infected-count", ("count", state.InfectedCount));
            PointsPerSecondLabel.Text = Loc.GetString("disease-evolution-points-per-second", ("points", state.PointsPerSecond));

            DiseaseHealthLabel.Visible = true;
            InfectivityLabel.Visible = true;
            InfectedCountLabel.Visible = true;
            PointsPerSecondLabel.Visible = true;
        }

        _selectedActiveSymptom = null;
        _selectedActiveBody = null;

        DeleteSymptomButton.Disabled = true;
        DeleteBodyButton.Disabled = true;

        if (state.HasDisease == false)
        {
            DiseaseDataMissing.Visible = true;
            WhitelistDiseaseDataMissing.Visible = true;

            EvolutionServerMissing.Visible = false;
            EvolutionAnalyzerMissing.Visible = false;
            EvolutionServerFar.Visible = false;
            EvolutionAnalyzerFar.Visible = false;
            EvolutionContents.Visible = false;

            WhitelistServerMissing.Visible = false;
            WhitelistAnalyzerMissing.Visible = false;
            WhitelistServerFar.Visible = false;
            WhitelistAnalyzerFar.Visible = false;
            WhitelistContents.Visible = false;

            BuyBodyButton.Visible = false;
            BuySymptomButton.Visible = false;
            AvailableSymptomsList.Visible = false;

            return;
        }
        else
        {
            DiseaseDataMissing.Visible = false;
            WhitelistDiseaseDataMissing.Visible = false;
        }

        var serverMissing = !state.DataServerConnected;
        var analyzerMissing = !state.SolutionAnalyzerConnected;
        var serverFar = state.DataServerConnected && !state.DataServerInRange;
        var analyzerFar = state.SolutionAnalyzerConnected && !state.SolutionAnalyzerInRange;
        var hasError = serverMissing || analyzerMissing || serverFar || analyzerFar;

        EvolutionServerMissing.Visible = serverMissing;
        EvolutionAnalyzerMissing.Visible = analyzerMissing;
        EvolutionServerFar.Visible = serverFar;
        EvolutionAnalyzerFar.Visible = analyzerFar;
        EvolutionContents.Visible = !hasError;

        WhitelistServerMissing.Visible = serverMissing;
        WhitelistAnalyzerMissing.Visible = analyzerMissing;
        WhitelistServerFar.Visible = serverFar;
        WhitelistAnalyzerFar.Visible = analyzerFar;
        WhitelistContents.Visible = !hasError;

        BuyBodyButton.Visible = !hasError;
        BuySymptomButton.Visible = !hasError;
        AvailableSymptomsList.Visible = !hasError;

        var diseaseSystem = _entityManager.System<DiseaseSystem>();

        // Активные симптомы
        ActiveSymptomsList.Clear();
        if (state.ActiveSymptoms != null)
        {
            foreach (var active in state.ActiveSymptoms)
            {
                var price = diseaseSystem.GetSymptomDeletePrice(state.MultiPriceDeleteSymptom);
                if (_prototype.TryIndex(active, out var proto))
                {
                    ActiveSymptomsList.AddItem(
                        $"{proto.Name} ({price})",
                        metadata: active
                    );
                }
            }

            AvailableSymptomsList.Clear();
            _availableSymptoms.Clear();

            foreach (var proto in _prototype.EnumeratePrototypes<DiseaseSymptomPrototype>())
            {
                if (state.ActiveSymptoms.Contains(proto.ID))
                    continue;

                if (proto.DangerIndicator == DangerIndicatorSymptom.Cataclysm)
                    continue;

                var price = diseaseSystem.GetSymptomPrice(state.ActiveSymptoms, proto.ID);
                AvailableSymptomsList.AddItem(
                    $"{proto.Name} ({price})",
                    metadata: proto.ID
                );

                _availableSymptoms.Add(proto);
            }

        }
        if (state.SpeciesWhitelist != null)
        {
            ActiveBodiesList.Clear();
            foreach (var body in state.SpeciesWhitelist)
            {
                var price = diseaseSystem.GetBodyDeletePrice();
                if (_prototype.TryIndex(body, out var proto))
                {
                    ActiveBodiesList.AddItem(
                        $"{Loc.GetString(proto.Name)} ({price})",
                        metadata: body
                    );
                }
            }

            AvailableBodiesList.Clear();
            _availableBodies.Clear();

            foreach (var proto in _prototype.EnumeratePrototypes<SpeciesPrototype>())
            {
                if (state.SpeciesWhitelist.Contains(proto.ID))
                    continue;

                if (BaseDiseaseSettings.BodyBlackList.Contains(proto.ID))
                    continue;

                var price = diseaseSystem.GetBodyPrice(state.SpeciesWhitelist);

                AvailableBodiesList.AddItem(
                    $"{Loc.GetString(proto.Name)} ({price})",
                    metadata: proto.ID
                );

                _availableBodies.Add(proto);
            }

        }
    }

    private void OnAvailableSymptomSelected(ItemList.ItemListSelectedEventArgs args)
    {
        if (_lastUpdate == null)
            return;

        if (args.ItemIndex < 0)
        {
            BuySymptomButton.Disabled = true;
            SymptomCostLabel.Text = string.Empty;
            SymptomDescription.Clear();
            return;
        }

        if (args.ItemIndex >= _availableSymptoms.Count)
        {
            BuySymptomButton.Disabled = true;
            return;
        }

        var proto = _availableSymptoms[args.ItemIndex];

        SymptomDescription.SetMessage(proto.Description);

        var price = proto.Price
                    * Math.Max(1, _lastUpdate.ActiveSymptoms.Count);

        SymptomCostLabel.Text = Loc.GetString(
            "disease-evolution-cost-label",
            ("cost", price)
        );

        BuySymptomButton.Disabled = _lastUpdate.MutationPoints < price;
    }

    private void OnAvailableBodySelected(ItemList.ItemListSelectedEventArgs args)
    {
        if (_lastUpdate == null)
            return;

        if (args.ItemIndex < 0 || args.ItemIndex >= _availableBodies.Count)
        {
            BuyBodyButton.Disabled = true;
            BodyCostLabel.Text = string.Empty;
            return;
        }

        var proto = _availableBodies[args.ItemIndex];

        var price = BaseDiseaseSettings.StaticBodyPrice
                    * Math.Max(1, _lastUpdate.SpeciesWhitelist.Count);

        BodyCostLabel.Text = Loc.GetString(
            "disease-evolution-cost-label",
            ("cost", price)
        );

        BuyBodyButton.Disabled = _lastUpdate.MutationPoints < price;
    }

    private void OnActiveSymptomSelected(ItemList.ItemListSelectedEventArgs args)
    {
        _selectedActiveSymptom = null;
        DeleteSymptomButton.Disabled = true;

        if (_lastUpdate == null || args.ItemIndex < 0)
            return;

        var item = ActiveSymptomsList[args.ItemIndex];
        if (item.Metadata is not ProtoId<DiseaseSymptomPrototype> id)
            return;

        _selectedActiveSymptom = id;

        var diseaseSystem = _entityManager.System<DiseaseSystem>();
        var deletePrice = diseaseSystem.GetSymptomDeletePrice(
            _lastUpdate.MultiPriceDeleteSymptom
        );

        DeleteSymptomButton.Disabled = _lastUpdate.MutationPoints < deletePrice;
    }

    private void OnActiveBodySelected(ItemList.ItemListSelectedEventArgs args)
    {
        _selectedActiveBody = null;
        DeleteBodyButton.Disabled = true;

        if (_lastUpdate == null || args.ItemIndex < 0)
            return;

        var item = ActiveBodiesList[args.ItemIndex];
        if (item.Metadata is not ProtoId<SpeciesPrototype> id)
            return;

        _selectedActiveBody = id;

        var diseaseSystem = _entityManager.System<DiseaseSystem>();
        var deletePrice = diseaseSystem.GetBodyDeletePrice();

        DeleteBodyButton.Disabled = _lastUpdate.MutationPoints < deletePrice;
    }

}
